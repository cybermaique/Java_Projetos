package senai_2021_tp.aula2;

public class CalculadoraPagamentoParameter {
	public String nome;
	public int idade;
	public String dataNascimento;
	public float salario;

	public CalculadoraPagamentoParameter(String nome, int idade, String dataNascimento, float salario) {
		this.nome = nome;
		this.idade = idade;
		this.dataNascimento = dataNascimento;
		this.salario = salario;
	}
}